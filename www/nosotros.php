    <h2>Nuestros Valores</h2>
    <p>Somos jóvenes que compartimos con otros jóvenes. Lo hacemos informalmente, en un ambiente seguro, brindando una amistad 
        incondicional, y sirviendo para dar la oportunidad de escuchar de Jesucristo, con libertad para aceptarlo o rechazarlo. 
        Igual que él lo hizo con sus discípulos.</p>
    <p>
        <ul>
            <li>Vivimos de acuerdo con todo el Evangelio de Jesucristo y lo comunicamos.</li>
            <li>Cumplimos nuestra misión bajo la autoridad de la Escritura y apoyados en el Espíritu Santo para llevar a cabo nuestro ministerio.</li>
            <li>Promovemos el bienestar y el equilibrio espiritual de nuestros miembros, para que puedan acompañar a otros hacia  una relación sólida con Cristo.</li>
            <li>Desarrollamos nuevos acercamientos para alcanzar a jóvenes desinteresados en la Espiritualidad Cristiana.</li>
            <li>Alcanzamos a jóvenes de todos los trasfondos sociales, culturas, niveles económicos y  orígenes étnicos en Costa Rica.</li>
            <li>Damos la bienvenida a todos los que comparten nuestra visión. Personal y voluntarios vinculados al propósito de llevar a los jóvenes a Jesucristo.</li>
