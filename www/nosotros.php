    <h2>Historia</h2>
    <p>En 1981, Oldemar Escamilla y Vilma Delgado, miembros de la Renovación Carismática Católica de Lourdes, 
        conocieron a Marvin Asfahl y su esposa Elaine, misioneros de Young Life Estados Unidos y compartieron sus ideas sobre 
        trabajar con jóvenes. Desde entonces Dios le ha permitido a Vida Joven seguir llevando su mensaje a cientos de 
        costarricenses. </p>
    
    <h2>Staff Nacional</h2>
    <p>El Staff Nacional es un equipo comprometido de personas que laboran a tiempo completo y de manera remunerada para 
        Vida Joven - Young Life de Costa Rica. Estas personas planifican, proponen, lideran y desarrollan todo el trabajo 
        operativo que involucra nuestro ministerio. Colaboran estrechamente con nuestros Directores de Comunidad y nuestra 
        Junta Directiva.</p>
    
    <h2>Directores de Comunidad</h2>
    <p>Vida Joven - Young Life de Costa Rica nombra a un Director para cada comunidad en la que trabaja. Si desea conocer sobre el 
